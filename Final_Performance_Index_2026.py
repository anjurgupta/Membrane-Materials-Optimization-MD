def load_input_file(path: Path) -> pd.DataFrame:
    suffix = path.suffix.lower()
    if suffix == ".csv":
        return pd.read_csv(path)
    if suffix in {".xls", ".xlsx"}:
        return pd.read_excel(path)
    raise ValueError(f"Unsupported file type for '{path}'. Use CSV, XLS, or XLSX.")


def prepare_dataframe(df_raw: pd.DataFrame) -> pd.DataFrame:
    df = standardize_columns(df_raw)
    df["Material"] = df["Material"].astype(str).str.strip()

    numeric_cols = ["MaxTemp", "TensileStrength", "WVTR", "Conductivity", "EmbodiedEnergy", "CO2"]
    for col in numeric_cols:
        df[col] = coerce_numeric(df[col])

    df = df.dropna(how="all").reset_index(drop=True)

    if df[numeric_cols].isna().any().any():
        bad_rows = df[df[numeric_cols].isna().any(axis=1)]
        raise ValueError(
            "Some required numeric cells could not be read.\nProblem rows:\n"
            + bad_rows.to_string(index=False)
        )

    return df


def pick_input_file_from_notebook() -> Path:
    try:
        from google.colab import files

        print("Upload your CSV or Excel file.")
        uploaded = files.upload()
        filename = list(uploaded.keys())[0]
        return Path(filename)
    except Exception:
        raise ValueError(
            "No valid input file was provided. In terminal, pass a CSV/XLS/XLSX path. "
            "In Colab, run without arguments and upload the file when prompted."
        )


def resolve_input_file(arg_path: Path | None) -> Path:
    valid_suffixes = {".csv", ".xls", ".xlsx"}

    if arg_path is not None and arg_path.suffix.lower() in valid_suffixes:
        return arg_path

    # Jupyter often injects a kernel .json file as a fake positional arg
    if arg_path is not None and arg_path.suffix.lower() == ".json":
        return pick_input_file_from_notebook()

    if arg_path is None:
        return pick_input_file_from_notebook()

    raise ValueError(
        f"Unsupported input file '{arg_path}'. Use CSV, XLS, or XLSX."
    )


# ---------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------
def main() -> None:
    parser = argparse.ArgumentParser(description="Calculate membrane-material performance indices.")
    parser.add_argument("input_file", type=Path, nargs="?", help="Path to input CSV or Excel file.")
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("performance_index_outputs"),
        help="Directory for output CSV files.",
    )
    parser.add_argument(
        "--exclude-material",
        type=str,
        default="SBS",
        help="Material to exclude from the normalization pool for sensitivity calculation.",
    )

    # Use only real CLI args in terminal; ignore notebook kernel args
    known_args, _ = parser.parse_known_args()

    input_file = resolve_input_file(known_args.input_file)
    output_dir = known_args.output_dir
    exclude_material = known_args.exclude_material

    output_dir.mkdir(parents=True, exist_ok=True)

    raw_df = load_input_file(input_file)
    df = prepare_dataframe(raw_df)

    full_df = compute_performance_indices(df)
    full_out = output_dir / "performance_index_full.csv"
    full_df.to_csv(full_out, index=False)

    try:
        _, comparison_df = build_comparison(df, exclude_material=exclude_material)
        comparison_out = output_dir / f"performance_index_excluding_{exclude_material}.csv"
        comparison_df.to_csv(comparison_out, index=False)
        print(f"Saved:\n- {full_out}\n- {comparison_out}")
    except ValueError as exc:
        print(f"Saved:\n- {full_out}")
        print(f"Excluded-material comparison skipped: {exc}")


if __name__ == "__main__":
    main()